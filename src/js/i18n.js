// Portuguese labels for bilingual PDF
export const portugueseLabels = translations.pt;

let currentLanguage = 'en';

// Detect browser language
export function detectLanguage() {
  const browserLang = navigator.language || navigator.languages?.[0] || 'en';
  const langCode = browserLang.split('-')[0].toLowerCase();
  
  if (translations[langCode]) {
    return langCode;
  }
  return 'en';
}

// Get saved language or detect
export function getInitialLanguage() {
  const saved = localStorage.getItem('sef-form-language');
  if (saved && translations[saved]) {
    return saved;
  }
  return detectLanguage();
}

// Set current language
export function setLanguage(lang) {
  if (!translations[lang]) {
    lang = 'en';
  }
  currentLanguage = lang;
  localStorage.setItem('sef-form-language', lang);
  applyTranslations();
  document.documentElement.lang = lang;
}

// Get current language
export function getCurrentLanguage() {
  return currentLanguage;
}

// Get translation
export function t(key) {
  return translations[currentLanguage]?.[key] || translations.en[key] || key;
}

// Get bilingual label for PDF
export function getBilingualLabel(key) {
  const userLang = translations[currentLanguage]?.[key] || translations.en[key] || key;
  const ptLang = portugueseLabels[key] || key;
  
  if (currentLanguage === 'pt') {
    return ptLang;
  }
  return `${userLang} / ${ptLang}`;
}

// Apply translations to DOM
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  
  // Update select options
  document.querySelectorAll('select option[data-i18n]').forEach(option => {
    const key = option.getAttribute('data-i18n');
    option.textContent = t(key);
  });
}

// Initialize i18n
export function initI18n() {
  const lang = getInitialLanguage();
  setLanguage(lang);
  
  // Set language selector value
  const selector = document.getElementById('language-select');
  if (selector) {
    selector.value = lang;
    selector.addEventListener('change', (e) => {
      setLanguage(e.target.value);
    });
  }
}
